        public static string GetMimeMapping(string filename) {
            var extension = Path.GetExtension(filename) ?? ".*";
            return ExtensionToMimeMappingTable.ContainsKey(extension)
                ? ExtensionToMimeMappingTable[extension]
                : ExtensionToMimeMappingTable[".*"];
        }
